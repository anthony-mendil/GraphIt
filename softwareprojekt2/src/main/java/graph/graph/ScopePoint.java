package graph.graph;

/**
 * The positions 1 to 4 described the 4 quarters of a vertex. If edges with the same arrowhead are located in such a
 * quarter, the arrows are combined by rendering and displayed only once.
 */
public enum ScopePoint {
    /**
     * the first quarter
     */
    POS_1,
    /**
     * the second quarter
     */
    POS_2,
    /**
     * the third quarter
     */
    POS_3,
    /**
     * the fourth quarter
     */
    POS_4
}
